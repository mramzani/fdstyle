    /**
     * Define the model's default state.
     *
     * @return array
     */
    public function definition():array
    {
        return [
            'name' => $this->product(),
            'slug' => $this->productSlug(),
            'barcode' => Common::NumericGenerator(10),
        ];
    }

    protected function product():string
    {
        return $this->getRandomKey('product');
    }

    protected function productSlug():string
    {
        $name = $this->getRandomKey('product');
        return Common::SlugCreator($name);
    }

    private function getRandomKey($object = null): string
    {
        if (is_string($object)) {
            $array = $this->objects[$object];
            $name = array_rand($array);
        }

        return (string)$array[$name];
    }
